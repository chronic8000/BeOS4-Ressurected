// the largest input block should be 4610 bytes
status_t ntfs_decompress_block(void *buf, void *bufout, uint32 *indelta, uint32 *outdelta, uint32 max_outbytes)
{
	uchar *inbuf = (uchar *)buf;
	uchar *outbuf = (uchar *)bufout;
	uint16 block_length;
	int pos, outpos = 0;
	int i;
//	uint16 lmask, dshift;
	uint8	tag_byte=0;	
	int tag_byte_pos;
	int delta, len;
	uint16 temp;
	
	*outdelta = *indelta = 0;
	
	// Calculate the block length
	temp = B_LENDIAN_TO_HOST_INT16(*(uint16 *)inbuf);
	block_length = (0x0FFF & temp) + 1;

//	dprintf("top uint16 in compressed stream is 0x%04x\n", temp);

	if(temp == 0) return B_NO_ERROR;

	// See if it is not compressed
	if((temp & 0x8000) == 0) {
		// It isn't compressed, just copy it
//		dprintf("not compressed\n");
		if(block_length) {
			memcpy(bufout, (char *)buf+2, block_length);
		}
		*indelta = block_length + 2;
		*outdelta = block_length;
		return B_NO_ERROR;
	}
				
//	dprintf("block length = %d.\n", block_length);	
	
	tag_byte_pos = 0;
	for(pos=2; pos < block_length+2; pos++) {
		if(tag_byte_pos == 0) {
			tag_byte = inbuf[pos];
		//	printf("tag byte = %x.\n", tag_byte);				
			tag_byte_pos = 8;
		} else {
			if(tag_byte & 1) {
				// The next two bytes are a back-reference
/*				for(i=outpos-1,lmask=0xfff,dshift=12;i>=0x10;i>>=1) {			
					lmask >>= 1;
					dshift--;
				}*/
				// Calculate the back reference spot
				temp = B_LENDIAN_TO_HOST_INT16(*(uint16 *)(&inbuf[pos]));
				delta = temp >> dshift[outpos];
				len = (temp & lmask[outpos]) + 3;					
//				delta = temp >> dshift;
//				len = (temp & lmask) + 3;			
				// Copy it
				for(i=0; i<len; i++) {
					outbuf[outpos] = outbuf[outpos-delta-1];
					outpos++;
					if(outpos == max_outbytes) goto out;
				}
				pos++;	
			} else {
				// This byte is not compressed 
				outbuf[outpos++] = inbuf[pos];
				if(outpos == max_outbytes) goto out;
			}
			tag_byte >>= 1;	
			tag_byte_pos--;
		}
		if(outpos == max_outbytes) goto out;
	}	


out:
	*indelta = block_length + 2;
	*outdelta = outpos;
	
	return B_NO_ERROR;
}

