//////////////////////////////////////////////////////////////////////////////
// Lock the Driver
//    This function gains exclusive access to the driver variables.

void lock_driver(void)
{
  ACQUIRE_GDS_BENAPHORE(&(driver_data.driver_benaphore));
}


//////////////////////////////////////////////////////////////////////////////
// Unlock the Driver
//    This function releases exclusive access to the driver variables.

void unlock_driver(void)
{
  RELEASE_GDS_BENAPHORE(&(driver_data.driver_benaphore));
}


